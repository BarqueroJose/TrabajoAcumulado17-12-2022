    private void tablaEmpleadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaEmpleadosMouseClicked
        // TODO add your handling code here:
         int fila_seleccionada = tablaEmpleados.getSelectedRow();
        txtempleados.setText(tablaEmpleados.getValueAt(fila_seleccionada, 0).toString());
        txtCargo.setText(tablaEmpleados.getValueAt(fila_seleccionada, 1).toString());
        cbodepartamento.setSelectedItem(tablaEmpleados.getValueAt(fila_seleccionada, 2).toString());
        txtjefe.setText(tablaEmpleados.getValueAt(fila_seleccionada, 3).toString());
        txtcodigo.setText(tablaEmpleados.getValueAt(fila_seleccionada,4).toString().toString());
                  filas = fila_seleccionada;
    }//GEN-LAST:event_tablaEmpleadosMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int fila_seleccionada = tablaEmpleados.getSelectedRow();
        if (fila_seleccionada >= 0) {
            model.removeRow(fila_seleccionada);
        } else {
            JOptionPane.showMessageDialog(null, "Selecione la fila correcta");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed
     public void limpiar(){
         this.txtempleados.setText("");
         this.txtCargo.setText("");
         this.cbodepartamento.setSelectedIndex(0);
         this.txtjefe.setText("");
         this.txtcodigo.setText("");
         txtempleados.grabFocus();
     }
