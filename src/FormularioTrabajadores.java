    private void btnagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarActionPerformed
        // TODO add your handling code here:
        this.model.addRow(new Object[]{this.txtempleados.getText(),this.txtCargo.getText(),this.cbodepartamento.getSelectedItem()      
        ,this.txtjefe.getText(),this.txtcodigo.getText()});
        
        limpiar();
        
    }//GEN-LAST:event_btnagregarActionPerformed
int filas;
    private void btnmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificarActionPerformed
        // TODO add your handling code here:
        String datos []= new String[5];
        datos[0] = this.txtempleados.getText();
        datos[1] = this.txtCargo.getText();
        datos[2] = this.cbodepartamento.getSelectedItem().toString();
        datos[3] = this.txtjefe.getText();
        datos[4] = this.txtcodigo.getText();
        
        for (int i = 0; i < tablaEmpleados.getColumnCount(); i++) {
            model.setValueAt(datos[i], filas, i);
        }
    }//GEN-LAST:event_btnmodificarActionPerformed

