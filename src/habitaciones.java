    private void btneditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditarActionPerformed
        // TODO add your handling code here:
        String datos[] = new String[7];
        datos[0] = txtnumeroHabitacion.getText();
        datos[1] = this.cbopiso.getSelectedItem().toString();
        datos[2] = this.cbodescripcion.getSelectedItem().toString();
        datos[3] = this.cboCaracteristica.getSelectedItem().toString();
        datos[4] = this.txtprecio.getText();
        datos[5] = this.cboEstado.getSelectedItem().toString();
        datos[6] = this.cboTipoHabitacion.getSelectedItem().toString();

        for (int i = 0; i < TablaHabitaciones.getColumnCount(); i++) {
            model.setValueAt(datos[i], filas, i);
        }
    }//GEN-LAST:event_btneditarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        this.model.addRow(new Object[]{this.txtnumeroHabitacion.getText(), this.cbopiso.getSelectedItem(),
            this.cbodescripcion.getSelectedItem(), this.cboCaracteristica.getSelectedItem(),
            this.txtprecio.getText(), this.cboEstado.getSelectedItem(), this.cboTipoHabitacion.getSelectedItem()});
         contador();
         limpiar();
        
    }//GEN-LAST:event_btnGuardarActionPerformed

