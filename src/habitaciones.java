    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:
        int fila_seleccionada = TablaHabitaciones.getSelectedRow();
        if (fila_seleccionada >= 0) {
            model.removeRow(fila_seleccionada);
        } else {
            JOptionPane.showMessageDialog(null, "Selecione la fila correcta");
        }
        contador();
    }//GEN-LAST:event_btneliminarActionPerformed

    private void TablaHabitacionesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaHabitacionesMouseClicked
        // TODO add your handling code here:
        int fila_seleccionada = TablaHabitaciones.getSelectedRow();
        txtnumeroHabitacion.setText(TablaHabitaciones.getValueAt(fila_seleccionada, 0).toString());
        cbopiso.setSelectedItem(TablaHabitaciones.getValueAt(fila_seleccionada, 1).toString());
        cbodescripcion.setSelectedItem(TablaHabitaciones.getValueAt(fila_seleccionada, 2).toString());
        cboCaracteristica.setSelectedItem(TablaHabitaciones.getValueAt(fila_seleccionada, 3).toString());
        txtprecio.setText(TablaHabitaciones.getValueAt(fila_seleccionada,4).toString().toString());
        cboEstado.setSelectedItem(TablaHabitaciones.getValueAt(fila_seleccionada,5).toString());
        cboTipoHabitacion.setSelectedItem(TablaHabitaciones.getValueAt(fila_seleccionada, 6).toString());
                  filas = fila_seleccionada;


    }//GEN-LAST:event_TablaHabitacionesMouseClicked

